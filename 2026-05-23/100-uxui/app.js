function renderVisualExamples(reference) {
  const examples = reference.gallery.length
    ? reference.gallery
    : [{ image: reference.preview, imageLarge: reference.preview, alt: reference.previewAlt }];

  return examples
    .filter((example) => example.image)
    .slice(0, 6)
    .map(
      (example, index) => `
        <a class="visual-example" href="${escapeHtml(example.imageLarge || example.image)}" target="_blank" rel="noreferrer">
          <img src="${escapeHtml(example.image)}" alt="${escapeHtml(example.alt)}" loading="${index === 0 ? "eager" : "lazy"}" />
          <span>${escapeHtml(example.alt)}</span>
        </a>
      `
    )
    .join("");
}

function openDrawer(referenceId) {
  const reference = references.find((item) => item.id === referenceId);
  if (!reference) return;

  activeReference = reference;

  drawerArt.className = `drawer-art ${reference.preview ? "has-preview" : getArtClass(reference)}`;
  drawerArt.style.setProperty("--art-bg", reference.color);
  drawerArt.style.setProperty("--art-accent", reference.accent);
  drawerArt.innerHTML = reference.preview
    ? `<img src="${escapeHtml(reference.preview)}" alt="${escapeHtml(reference.previewAlt)}" loading="eager" /><span class="shot-badge">Main screenshot</span>`
    : "";
  drawerMeta.textContent = `#${String(reference.id).padStart(3, "0")} · ${typeLabels[reference.type]} · ${patternByKey[reference.pattern].label}`;
  drawerTitle.textContent = reference.title;
  drawerTags.innerHTML = reference.tags.map((tag) => `<span>${escapeHtml(tag)}</span>`).join("");
  drawerVisuals.innerHTML = renderVisualExamples(reference);
  drawerUx.textContent = reference.ux;
  drawerVisual.textContent = reference.visualNotes.length
    ? `${reference.visual} Примеры из источника: ${reference.visualNotes.join(" ")}`
    : reference.visual;
  drawerApply.textContent = reference.apply;
  drawerRisk.textContent = reference.risk;
  drawerPortfolio.href = reference.portfolioUrl || reference.sourceUrl;
  drawerSource.href = reference.sourceUrl;

  drawer.classList.add("is-open");
  drawer.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
}

function closeDrawer() {
  drawer.classList.remove("is-open");
  drawer.setAttribute("aria-hidden", "true");
  document.body.style.overflow = "";
}

function copyText(text) {
  if (navigator.clipboard && window.isSecureContext) {
    return navigator.clipboard.writeText(text);
  }

  const textarea = document.createElement("textarea");
  textarea.value = text;
  textarea.setAttribute("readonly", "");
  textarea.style.position = "fixed";
  textarea.style.opacity = "0";
  document.body.append(textarea);
  textarea.select();
  document.execCommand("copy");
  textarea.remove();
  return Promise.resolve();
}

function getBrief(reference) {
  return [
    `${reference.title}`,
    `Паттерн: ${patternByKey[reference.pattern].label}`,
    `Портфолио: ${reference.portfolioUrl || reference.sourceUrl}`,
    `Разбор источника: ${reference.sourceUrl}`,
    `UX/UI: ${reference.ux}`,
    `Визуально: ${reference.visual}`,
    reference.visualNotes.length ? `Визуальные примеры: ${reference.visualNotes.join(" ")}` : "",
    `Применить: ${reference.apply}`,
    `Риск: ${reference.risk}`,
  ]
    .filter(Boolean)
    .join("\n");
}

